// for keyboard inputs
var keyDown = {
    37 : false,
    38 : false,
    39 : false,
    40 : false
};

var player = new Character();


function Character()
{
    this.tilePosition = [1,1];
    this.timeLastMoved = 0;
    this.MoveSpeed = 100;
}

Character.prototype.placeAt = function(x,y) {
    if (x <= gameMap.length-1 && x >= 0 && y <= gameMap.length-1 && y >= 0)
    {
        this.tilePosition[0] = x;
        this.tilePosition[1] = y;

        gameMap[x][y] = 2;

        console.log("DING: " + this.tilePosition);
    }
};

// moves the player to the given x and y pos if it is walkable.
Character.prototype.movePlayerTo = function(x,y) {
    // if the target x and y pos is walkable then move the player that pos.
    if (gameMap[x][y] == 0)
    {
        player.placeAt(x,y)
        player.timeLastMoved = Date.now(); 
    }
};

window.onload = function(){

    player.placeAt(Math.floor(gameMap.length / 2), Math.floor(gameMap.length / 2));


    context = this.document.getElementById('gameCanvas').getContext('2d');
    requestAnimationFrame(drawGame);
    context.font = "bold 10pt sans.serif";

    // match tile to canvase size
    mapW = gameMap[0].length;
    mapH = gameMap.length;

    tileW = (tileW / this.mapW) * 50;
    tileH = (tileH / this.mapH) * 50;

    window.addEventListener("keydown", function(e)
    {
         if (e.keyCode>=37 && e.keyCode<=40)
         {
            keyDown[e.keyCode] = true;
            console.log("KeyDown: " + keyDown[e.keyCode]);
         }
    });
    window.addEventListener("keyup", function(e){
        if (e.keyCode >=37 && e.keyCode<=40)
            keyDown[e.keyCode] = false; 
    });
};

function drawGame()
{
    if (context == null) {return;} // if the windwos has not loaded yet then we need to wait.
    
    // calculate the current seccond.
    var sec = Math.floor(Date.now()/1000);
    if (sec != currentSecond) 
    {
        // update currentSecond and asign frameCount.
        currentSecond = sec;
        frameLastSecond = frameCount;
        frameCount = 1;
    }
    else { frameCount ++; } // else we just need to increase the frame count.
    // since a lot of frames can happen within a seccond, there is a lot of waiting.
    // this is why I use a frame count.
    
    if (Date.now() > player.timeLastMoved + player.MoveSpeed)
    {

        // find out if any arrow keys are being pessed and if we can move in that direction .
        var posX =  player.tilePosition[0];
        var posY = player.tilePosition[1]

        if (keyDown[38] && gameMap[posX][posY-1] == 0)
        { 
            console.log("DING1");
            gameMap[posX][posY] = 0
            player.movePlayerTo(posX,posY-1)
            player.timeLastMoved = Date.now();
        }
        else if (keyDown[40] &&  gameMap[posX][posY+1] == 0)
        { 
            console.log("DING2");
            gameMap[posX][posY] = 0
            player.movePlayerTo(posX,posY+1)
            player.timeLastMoved = Date.now();
        }
        else if (keyDown[37] &&  gameMap[posX-1][posY] == 0)
        { 
            console.log("DING3");
            gameMap[posX][posY] = 0
            player.movePlayerTo(posX-1,posY)
            player.timeLastMoved = Date.now();
        }
        else if (keyDown[39] &&  gameMap[posX+1][posY] == 0)
        { 
            console.log("DING4");
            gameMap[posX][posY] = 0
            player.movePlayerTo(posX+1,posY)
            player.timeLastMoved = Date.now();
        }

    }

    // draw the tiles that make up the gameMap.
    for(var y = 0; y < mapH; y++)
    {
        for(var x = 0; x < mapW; x++)
        {
            // need to repace the 1d array with a 2d array later.
            switch (gameMap[x][y]) // this will get use the correct pos in the 1d array
            {
                case 1:
                    context.fillStyle = "#525e5e"; // if wall then fill with dark gray.
                    break;
                case 2:
                    context.fillStyle = "#0000ff";
                    break;
                default:
                    context.fillStyle = "#18ad0a"; // else the tile is a path.   
            }

            context.fillRect(x*tileW, y*tileH, tileW, tileH);
        }
    }
    context.fillStyle = "#ff0000";
    context.fillText("FPS: " + frameLastSecond, 10, 20);

    requestAnimationFrame(drawGame);
}

